import {Routes, Route, Navigate } from "react-router-dom";
import { useAuth } from "./hooks/useAuth";
import Login from "./pages/auth/Login";
import AdminRoutes from "./routes/AdminRoutes";
import Dashboard from "./pages/usuario/Dashboard";
import UserRoutes from "./routes/UserRoutes";

function App() {
  const { role, isAuthenticated } = useAuth();

    // ‚úÖ Debug cr√≠tico
  console.log('üîê App render - isAuthenticated:', isAuthenticated, 'role:', role);

  return (
      <Routes>
        {/* Si no est√° autenticado, mostrar Login en todas las rutas */}
        {!isAuthenticated ? (
          <Route path="*" element={<Login />} />
        ) : (
          /* Si est√° autenticado, mostrar las rutas correspondientes */
          <>
            {/* ‚úÖ Rutas del admin */}
            {role === "admin" && (
              <Route path="/admin/*" element={<AdminRoutes />} />
            )}

            {/* ‚úÖ Rutas del usuario normal */}
            {(role === "user" || role === "empleado") && (
              <Route path="/usuario/*" element={<UserRoutes />} />
            )}

            {/* Redirecci√≥n por defecto para autenticados */}
            <Route 
              path="*" 
              element={
                <Navigate to={
                  role === "admin" ? "/admin/dashboard" : "/usuario/dashboard"
                } />
              } 
            />
          </>
        )}
      </Routes>
  );
}

export default App;